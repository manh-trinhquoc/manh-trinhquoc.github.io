/*
Bài 3. Viết function printSpecialTriangle(height), với height là số integer > 1,
 in ra hình tam giác cân ngược với chiều cao bằng height. Ví dụ: 
// Với n = 2
* * * 
  * 

// Với n = 3
* * * * *
  * * *
    *
// Với n = 4
* * * * * * *
  * * * * *
    * * *
      *
*/
function printSpecialTriangle(height) {
    let result = "Bài 3: <pre>";
    for (let n = height; n > 0; n--) {
        for (let i = 0; i < height - n; i++) {
            result += "  ";
        }
        for (let i = 0; i < 2*n - 1; i++) {
            result += "* ";
        }
        result += "<br>";
    }
    result += "</pre>";
    document.write (result);
}

