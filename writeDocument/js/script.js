/*
Bài 2. Viết function printEmptyRectangle(width, height), với width và height 
là 2 số integer > 3, in ra một hình chữ nhật rỗng có kích thước width x height. 
Ví dụ: 

// Với a = 5, b = 3
* * * * *
*       *
* * * * *
*/
function printEmptyRectangle(width, height) {
    result = "Bài 2:<pre>"
    for (let n = 0; n < height; n++) {
        for (let i = 0; i < width; i++) {
            if (n == 0 || n == height - 1) result += '* ';
            else if (i == 0 || i == width - 1) result += '* ';
            else result += '  ';
        }
        result += "<br>";
    }
    result += "</pre>";
    document.write (result);
}

