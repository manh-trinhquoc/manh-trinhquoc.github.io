/*
Bài 5. Viết function printButterfly(height), với height là số integer > 4 và 
là số lẻ, in ra hình con bướm có chiều cao bằng height. Ví dụ: 

// Với n = 5
*       *
* *   * *
* * * * *
* *   * *
*       *
*/

function printButterfly(height) {
    let result = "Bài 5: <pre>";
    for (let n = 0; n < height; n++) {
        for (let i = 0; i < height; i++) {
            let nBackWard = height - 1 - n;
            if (i <= Math.min(n, nBackWard) || i >= Math.max(n, nBackWard)) result += "* ";
            else result += "  ";
        }
        result += "<br>";
    }
    result +="</pre>";
    document.write(result);
}

